"""Typical system-level units of both binary and SI prefix."""

kibibyte = 1 << 10
mebibyte = 1 << 20
gibibyte = 1 << 30
tebibyte = 1 << 40
pebibyte = 1 << 50

KiB = kibibyte
MiB = mebibyte
GiB = gibibyte
TiB = tebibyte
PiB = pebibyte

kilo = 10**3
mega = 10**6
giga = 10**9
tera = 10**12
peta = 10**15

milli = 10**-3
micro = 10**-6
nano  = 10**-9

KB = kilo
MB = mega
GB = giga
TB = tera
PB = peta

KHz = kilo
MHz = mega
GHz = giga

ms = milli
us = micro
ns = nano
